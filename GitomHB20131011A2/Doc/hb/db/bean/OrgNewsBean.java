package com.gitom.hb.db.bean;


public class OrgNewsBean extends AbstractBean {
	private long organizationId;
	private int orgunitId;
	private String username;
	private String newsType;
	private int newsSeq;
	private boolean voidFlag;
	private String content;
	private String title;
	
	public long getOrganizationId() {
		return organizationId;
	}

	public void setOrganizationId(long organizationId) {
		this.organizationId = organizationId;
	}

	public int getOrgunitId() {
		return orgunitId;
	}

	public void setOrgunitId(int orgunitId) {
		this.orgunitId = orgunitId;
	}

	public int getNewsSeq() {
		return newsSeq;
	}

	public void setNewsSeq(int newsSeq) {
		this.newsSeq = newsSeq;
	}
	
	public String getNewsType() {
		return newsType;
	}

	public void setNewsType(String newsType) {
		this.newsType = newsType;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public boolean getVoidFlag() {
		return voidFlag;
	}

	public void setVoidFlag(boolean voidFlag) {
		this.voidFlag = voidFlag;
	}
}
